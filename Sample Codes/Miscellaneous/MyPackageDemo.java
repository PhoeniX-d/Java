package Arithmatic;

public class MyPackageDemo
{
    public int add(int n1, int n2)
    {
        return n1 + n2;
    }

    public int sub(int n1, int n2)
    {
        return n1 - n2;
    }

    public int prod(int n1, int n2)
    {
        return n1 * n2;
    }

    public int div(int n1, int n2)
    {
        return n1 / n2;
    }
}